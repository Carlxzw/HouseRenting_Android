package com.example.administer.houserenting_android.model;

public class RoomInfo {
	private String roomNo;

	private String roomAddress;

	private String roomCover;

	private String roomArea;

	private String roomPrice;

	private String roomBackup;

	private String roomState;

	private String roomType;

	private String roomTitle;

	private RoomDevice roomDevice;


	public String getRoomNo() {
		return roomNo;
	}

	public void setRoomNo(String roomNo) {
		this.roomNo = roomNo;
	}

	public String getRoomAddress() {
		return roomAddress;
	}

	public void setRoomAddress(String roomAddress) {
		this.roomAddress = roomAddress;
	}

	public String getRoomCover() {
		return roomCover;
	}

	public void setRoomCover(String roomCover) {
		this.roomCover = roomCover;
	}

	public String getRoomArea() {
		return roomArea;
	}

	public void setRoomArea(String roomArea) {
		this.roomArea = roomArea;
	}

	public String getRoomPrice() {
		return roomPrice;
	}

	public void setRoomPrice(String roomPrice) {
		this.roomPrice = roomPrice;
	}

	public String getRoomBackup() {
		return roomBackup;
	}

	public void setRoomBackup(String roomBackup) {
		this.roomBackup = roomBackup;
	}

	public String getRoomState() {
		return roomState;
	}

	public void setRoomState(String roomState) {
		this.roomState = roomState;
	}

	public String getRoomType() {
		return roomType;
	}

	public void setRoomType(String roomType) {
		this.roomType = roomType;
	}

	public String getRoomTitle() {
		return roomTitle;
	}

	public void setRoomTitle(String roomTitle) {
		this.roomTitle = roomTitle;
	}

	public RoomDevice getRoomDevice() {
		return roomDevice;
	}

	public void setRoomDevice(RoomDevice roomDevice) {
		this.roomDevice = roomDevice;
	}
}
